public class ClazzBean {
    private String clazzName;

    public ClazzBean(String clazzName) {
        this.clazzName = clazzName;
    }
    public ClazzBean() {
        this.clazzName = clazzName;
    }

    public String getClazzName() {
        return clazzName;
    }

    public void setClazzName(String clazzName) {
        this.clazzName = clazzName;
    }
}
